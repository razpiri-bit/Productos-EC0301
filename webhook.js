// Generar c√≥digo de acceso √∫nico
function generarCodigoAcceso() {
  const chars = 'ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789';
  let codigo = '';
  for (let i = 0; i < 12; i++) {
    codigo += chars.charAt(Math.floor(Math.random() * chars.length));
    if (i === 3 || i === 7) codigo += '-';
  }
  return codigo;
}

// Validar y formatear tel√©fono mexicano
function validarTelefono(telefono) {
  if (!telefono) return null;
  
  // Limpiar el n√∫mero
  const cleaned = telefono.replace(/\D/g, '');
  
  // Formatos aceptados:
  // 5538822334 (10 d√≠gitos) ‚Üí 525538822334
  // 525538822334 (12 d√≠gitos) ‚Üí 525538822334
  // +525538822334 (con +52) ‚Üí 525538822334
  
  if (cleaned.length === 10) {
    return `52${cleaned}`; // Agregar c√≥digo de M√©xico
  } else if (cleaned.length === 12 && cleaned.startsWith('52')) {
    return cleaned;
  } else if (telefono.startsWith('+52')) {
    return cleaned;
  }
  
  return null; // Formato inv√°lido
}

